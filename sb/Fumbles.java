package com.sb;

public class Fumbles {

	String name = "";
	String tot = "";
	String rcv = "";
	String trcv = "";
	String yds = "";
	String lost = "";
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTot() {
		return tot;
	}
	public void setTot(String tot) {
		this.tot = tot;
	}
	public String getRcv() {
		return rcv;
	}
	public void setRcv(String rcv) {
		this.rcv = rcv;
	}
	public String getTrcv() {
		return trcv;
	}
	public void setTrcv(String trcv) {
		this.trcv = trcv;
	}
	public String getYds() {
		return yds;
	}
	public void setYds(String yds) {
		this.yds = yds;
	}
	public String getLost() {
		return lost;
	}
	public void setLost(String lost) {
		this.lost = lost;
	}
	
}
