package com.sb;

public class Passing {

	String name = "";
	String att = "";
	String cmp = "";
	String yds = "";
	String tds = "";
	String ints = "";
	String twopta = "";
	String twoptm = "";
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAtt() {
		return att;
	}
	public void setAtt(String att) {
		this.att = att;
	}
	public String getCmp() {
		return cmp;
	}
	public void setCmp(String cmp) {
		this.cmp = cmp;
	}
	public String getYds() {
		return yds;
	}
	public void setYds(String yds) {
		this.yds = yds;
	}
	public String getTds() {
		return tds;
	}
	public void setTds(String tds) {
		this.tds = tds;
	}
	public String getInts() {
		return ints;
	}
	public void setInts(String ints) {
		this.ints = ints;
	}
	public String getTwopta() {
		return twopta;
	}
	public void setTwopta(String twopta) {
		this.twopta = twopta;
	}
	public String getTwoptm() {
		return twoptm;
	}
	public void setTwoptm(String twoptm) {
		this.twoptm = twoptm;
	}
}
