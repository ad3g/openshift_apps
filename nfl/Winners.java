package com.nfl;

public class Winners {
	
	private String yr;
	private String seas;
	private String wk;
	private String player;
	private String amt;
	
	public String getYr() {
		return yr;
	}
	public void setYr(String yr) {
		this.yr = yr;
	}
	public String getSeas() {
		return seas;
	}
	public void setSeas(String seas) {
		this.seas = seas;
	}
	public String getWk() {
		return wk;
	}
	public void setWk(String wk) {
		this.wk = wk;
	}
	public String getPlayer() {
		return player;
	}
	public void setPlayer(String player) {
		this.player = player;
	}
	public String getAmt() {
		return amt;
	}
	public void setAmt(String amt) {
		this.amt = amt;
	}
	
	
}
